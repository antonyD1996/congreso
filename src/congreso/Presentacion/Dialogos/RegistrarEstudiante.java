    public void init() {

        btnAceptar.addActionListener(l -> {
            if (txtCodigo.getText().trim().isEmpty() || txtCodigo.getText().trim().length() >12) {
                JOptionPane.showMessageDialog(null, "El Codigo no es valido");
            } else {
                if (txtNombre.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "El Nombre no es valido");
                } else {
                    if (txtCarrera.getText().trim().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Ingrese una Carrera valida");
                    } else {
                        if (regional == null) {
                            JOptionPane.showMessageDialog(null, "Seleccione una Regional");
                        } else {
                            if (txtAbono.getText().isEmpty()) {
                                JOptionPane.showMessageDialog(null, "El campo de abono no puede estar vacio");
                            } else {
                                Estudiante e = new Estudiante();
                                e.setCodigo(txtCodigo.getText());
                                e.setNombre(txtNombre.getText());
                                e.setCarrera(txtCarrera.getText());
                                e.setRegional(regional);
                                estudiante = e;
                                abono = Integer.valueOf(txtAbono.getText());
                                setVisible(false);
                            }
                        }
                    }
                }
            }
        });
        btnCancelar1.addActionListener(l -> {
            setVisible(false);
            dispose();
        });

    }

