    public void init() {
        habilitar();
        mostrarCongresos.accept(tblCongresos);
        tblCongresos.setFillsViewportHeight(true);
        if (listadoModel == null) {
            listadoModel = new ArrayList<>();
        }

        btnAcept.addActionListener(l -> {
            RegistrarCongreso rc = new RegistrarCongreso(this, true);
            rc.setLocationRelativeTo(null);
            rc.setVisible(true);
            mostrarCongresos.accept(tblCongresos);
            
        });

        btnAdministrar.addActionListener(l -> {
            if (congreso != null) {
                AdminCongreso ac = new AdminCongreso(congreso);
                ac.setLocationRelativeTo(null);
                this.setVisible(false);
                ac.setVisible(true);
                this.dispose();
            }
        });

        tblCongresos.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                try {
                    int i = tblCongresos.getSelectedRow();
                    ID = Long.valueOf(tblCongresos.getValueAt(i, 0).toString());
                    listadoModel.stream().forEach(lm -> {
                        if (lm.getId().equals(ID)) {
                            congreso = lm;
                            habilitar();
                        }
                    });
                } catch (ArrayIndexOutOfBoundsException e1) {
                }
            }
        });

    }
    Consumer<JTable> mostrarCongresos = (t) -> {
        listadoModel = ci.actualizarDatos.apply(t);
    };

    public void habilitar() {
        if (congreso == null) {
            btnAdministrar.setEnabled(false);
        } else {
            btnAdministrar.setEnabled(true);
        }
    }

